// Kelas Ruangan
class Ruangan {
    private String namaRuangan;
    private int kapasitas;

    public Ruangan(String namaRuangan, int kapasitas) {
        this.namaRuangan = namaRuangan;
        this.kapasitas = kapasitas;
    }

    public String getNamaRuangan() {
        return namaRuangan;
    }
}